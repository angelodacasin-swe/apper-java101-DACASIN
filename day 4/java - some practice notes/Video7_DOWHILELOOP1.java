
class Video7_DOWHILELOOP1 {

    public static void main(String[] args) {

        int i = 6;

        do {
            System.out.println(i); //It will print i.
            i++;

        }while(i < 5); //do-while loop, code that executes AT LEAST 1 time. It will read i = 6 before reading while.
    }                  //do-while execution then condition ; while -- condition then execution
}
